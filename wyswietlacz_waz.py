from machine import Pin, PWM
from time import sleep_ms

s1 = Pin(0, Pin.OUT)
s2 = Pin(1, Pin.OUT)
s3 = Pin(2, Pin.OUT)
s4 = Pin(3, Pin.OUT)            
s5 = Pin(4, Pin.OUT)
s6 = Pin(5, Pin.OUT)
s7 = Pin(7, Pin.OUT)
s8 = Pin(6, Pin.OUT)    


def all_off():
    s1.off()
    s2.off()
    s3.off()
    s4.off()
    s5.off()
    s6.off()
    s7.off()
    s8.off()

def all_on():
    s1.on()
    s2.on()
    s3.on()
    s4.on()
    s5.on()
    s6.on()
    s7.on()
    s8.on()

czas = 100

while True:
    all_off()
    s1.on()
    s3.on()
    sleep_ms(czas)
    s1.off()
    s3.on()
    s4.on()
    sleep_ms(czas)
    s3.off()
    s4.on()
    s2.on()
    sleep_ms(czas)
    s4.off()
    s2.on()
    s5.on()
    sleep_ms(czas)
    s2.off()
    s5.on()
    s6.on()
    sleep_ms(czas)
    s5.off()
    s6.on()
    s7.on()
    sleep_ms(czas)
    s6.off()
    s7.on()
    s2.on()
    sleep_ms(czas)
    s7.off()
    s2.on()
    s1.on()
    sleep_ms(czas)
    s2.off()